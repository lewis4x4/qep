    classification,
    territory_code,
    county,
    status,
    notes,
    search_1,
    search_2,
    public.mask_customer_ein(ein) as ein,
    (ein is not null and not public.qrm_can_access_customer_ein()) as ein_masked,
    product_category,
    industry,
    size,
    owner_name,
    brand_of_interest,
    township,
    lot,
    concession,
    latitude,
    longitude,
    business_fax,
    business_cell,
    business_email,
    tax_code_equipment,
    tax_code_parts,
    tax_code_service,
    tax_code_rental,
    labor_tax_code_1,
    labor_tax_code_2,
    exempt_status_notes,
    avatax_entity_use_code,
    duns_number,
    naics_code,
    opt_out_sale_pi,
